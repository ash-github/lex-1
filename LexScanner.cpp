{
	// 	while(*code) code++;
	// 	//write(*code++);
	if(!code||!*code) return;
	ofstream outf;
	if(writeToMain) outf.open("_lexmain.cpp");
	else outf.open("_lblock3");
	while(*code)
		outf<<*code++;
	code=NULL;
	outf.close();
}
bool LexScanner::handleAction()
{
	char* p=yytext;
	int i=0;
	int bcnt=1;

	while(*code)
	{
		if(*code=='{') ++bcnt;
		else if(*code=='}'&&(--bcnt==0)) break;

		*p++=*code++;
		if(++i>LEXACTION_MAX_LENGTH) {printf("error: action exceeds maximum length!\n");*p=0;return false;}
	}
	*p=0;

	if(*code) code++;
	return true;
}